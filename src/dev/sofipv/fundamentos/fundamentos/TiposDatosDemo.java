package dev.sofipv.fundamentos.fundamentos;

import java.util.Scanner;

public class TiposDatosDemo {
    public static void run(Scanner scanner) {
        byte edad = 20;
        short anio = 2026;
        int poblacion = 125000;
        long distanciaLarga = 9876543210L;
        float temperatura = 36.6F;
        double pi = Math.PI;
        char inicial = 'S';
        boolean inscrita = true;
        String lenguaje = "Java";

        System.out.println("byte edad = " + edad);
        System.out.println("short anio = " + anio);
        System.out.println("int poblacion = " + poblacion);
        System.out.println("long distanciaLarga = " + distanciaLarga);
        System.out.println("float temperatura = " + temperatura);
        System.out.println("double pi = " + pi);
        System.out.println("char inicial = " + inicial);
        System.out.println("boolean inscrita = " + inscrita);
        System.out.println("String lenguaje = " + lenguaje);
    }
}
